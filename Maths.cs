using System;

public class Maths
{
    
    public static int MathOperation (int num1, int num2)
	{
		
		return num1 + num2;
	}

	public static decimal MathOperation (decimal num1, decimal num2, decimal num3)
	{
		
		return num1 + num2 + num3;
	}

	public static float MathOperation(float num1, float num2)
	{
		return (num1 * num2);
	}

    public static float MathOperation(float num1, float num2, float num3)
    {
        return (num1 * num2 * num3);
    }

}
